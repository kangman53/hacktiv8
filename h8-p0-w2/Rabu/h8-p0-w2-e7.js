//SOAL PERTAMA
var rows1 = 5;
for(var i=1;i<=rows1;i++){
  console.log('*');
}

//SOAL KEDUA
var rows2 = 5;
for(var j=1;j<=rows2;j++){
  var star1 = '';
  for(var k=1;k<=rows2;k++){
    star1 += '*';
  }
  console.log(star1);
}

//SOAL KETIGA
var rows3 = 5;
for(var l=1;l<=rows3;l++){
  var star2 = '';
  for(var m=1;m<=l;m++){
    star2 += '*';
  }
  console.log(star2);
}
